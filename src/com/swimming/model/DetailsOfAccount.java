package com.swimming.model;

public class DetailsOfAccount {
	String stu_name;
	int money;
	int times;
	String details;
	String account_operation_date;
	public String getstu_name() {
		return stu_name;
	}
	public void setstu_name(String stu_name) {
		this.stu_name = stu_name;
	}
	public int getMoney() {
		return money;
	}
	public void setMoney(int money) {
		this.money = money;
	}
	public int getTimes() {
		return times;
	}
	public void setTimes(int times) {
		this.times = times;
	}
	public String getAccount_operation_date() {
		return account_operation_date;
	}
	public void setAccount_operation_date(String account_operation_date) {
		this.account_operation_date = account_operation_date;
	}
	public String getDetails() {
		return details;
	}
	public void setDetails(String details) {
		this.details = details;
	}
}
