package com.swimming.model;

public class Course {
	int class_number;
	int coach_number;
	String class_name;
	int class_tab;
	String class_remark;
	String class_operation_time;
	String coach_name;
	public int getClass_number() {
		return class_number;
	}
	public void setClass_number(int class_number) {
		this.class_number = class_number;
	}
	public int getCoach_number() {
		return coach_number;
	}
	public void setCoach_number(int coach_number) {
		this.coach_number = coach_number;
	}
	public String getClass_name() {
		return class_name;
	}
	public void setClass_name(String class_name) {
		this.class_name = class_name;
	}
	public int getClass_tab() {
		return class_tab;
	}
	public void setClass_tab(int class_tab) {
		this.class_tab = class_tab;
	}
	public String getClass_remark() {
		return class_remark;
	}
	public void setClass_remark(String class_remark) {
		this.class_remark = class_remark;
	}
	public String getClass_operation_time() {
		return class_operation_time;
	}
	public void setClass_operation_time(String class_operation_time) {
		this.class_operation_time = class_operation_time;
	}
	public String getCoach_name() {
		return coach_name;
	}
	public void setCoach_name(String coach_name) {
		this.coach_name = coach_name;
	}
	
}
