package com.swimming.model;

public class Coach {
	int coach_number;
	String coach_name;
	String coach_sex;
	String coach_phone;
	String coach_birth_date;
	int coach_tab;
	String coach_remark;
	String coach_operation_date;
	public int getCoach_number() {
		return coach_number;
	}
	public void setCoach_number(int coach_number) {
		this.coach_number = coach_number;
	}
	public String getCoach_name() {
		return coach_name;
	}
	public void setCoach_name(String coach_name) {
		this.coach_name = coach_name;
	}
	public String getCoach_sex() {
		return coach_sex;
	}
	public void setCoach_sex(String coach_sex) {
		this.coach_sex = coach_sex;
	}
	public String getCoach_phone() {
		return coach_phone;
	}
	public void setCoach_phone(String coach_phone) {
		this.coach_phone = coach_phone;
	}
	public String getCoach_birth_date() {
		return coach_birth_date;
	}
	public void setCoach_birth_date(String coach_birth_date) {
		this.coach_birth_date = coach_birth_date;
	}
	public int getCoach_tab() {
		return coach_tab;
	}
	public void setCoach_tab(int coach_tab) {
		this.coach_tab = coach_tab;
	}
	public String getCoach_remark() {
		return coach_remark;
	}
	public void setCoach_remark(String coach_remark) {
		this.coach_remark = coach_remark;
	}
	public String getCoach_operation_date() {
		return coach_operation_date;
	}
	public void setCoach_operation_date(String coach_operation_date) {
		this.coach_operation_date = coach_operation_date;
	}
	
}
